package com.proflaut.dms.model;

public class ProprietorParDirRequest {

	private String name;
	private String dateOfBirth;
	private String fatherSpouse;
	private String academicQualification;
	private String mobileNo;
	private String panNo;
	private String residentialAddress;
	private String telNoRes;
	private String experience;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDateOfBirth() {
		return dateOfBirth;
	}

	public void setDateOfBirth(String dateOfBirth) {
		this.dateOfBirth = dateOfBirth;
	}

	public String getFatherSpouse() {
		return fatherSpouse;
	}

	public void setFatherSpouse(String fatherSpouse) {
		this.fatherSpouse = fatherSpouse;
	}

	public String getAcademicQualification() {
		return academicQualification;
	}

	public void setAcademicQualification(String academicQualification) {
		this.academicQualification = academicQualification;
	}

	public String getMobileNo() {
		return mobileNo;
	}

	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}

	public String getPanNo() {
		return panNo;
	}

	public void setPanNo(String panNo) {
		this.panNo = panNo;
	}

	public String getResidentialAddress() {
		return residentialAddress;
	}

	public void setResidentialAddress(String residentialAddress) {
		this.residentialAddress = residentialAddress;
	}

	public String getTelNoRes() {
		return telNoRes;
	}

	public void setTelNoRes(String telNoRes) {
		this.telNoRes = telNoRes;
	}

	public String getExperience() {
		return experience;
	}

	public void setExperience(String experience) {
		this.experience = experience;
	}

}
